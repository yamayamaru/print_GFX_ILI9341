#include <stdio.h>
#include "pico/stdlib.h"
#include "yama_2_GFX.h"

#define MOSI 19
#define SCK  18
#define CS   17
#define MISO 16

#define RST  22
#define DC   20

#define SPI_PORT  spi0

yama_2_GFX_ILI9341 tft = yama_2_GFX_ILI9341(SPI_PORT, DC, RST, CS);


extern const uint8_t bitmap01[];
extern const uint8_t bitmap_mask01[];

#define RASPBERRY_N  40
#define RDX        5
#define RDY        5

void random_raspberry(void);
void setup();
void loop();
int main() {
    stdio_init_all();

    spi_init(SPI_PORT, 40000000);
    gpio_set_function(MISO, GPIO_FUNC_SPI);
    gpio_set_function(SCK, GPIO_FUNC_SPI);
    gpio_set_function(MOSI, GPIO_FUNC_SPI);
    gpio_pull_up(MISO);
    gpio_pull_up(SCK);
    gpio_pull_up(MOSI);
    spi_set_format(SPI_PORT, 8, SPI_CPOL_0, SPI_CPHA_0, SPI_MSB_FIRST);   // SPI MODE0, MSBFIRST

    sleep_ms(100); 
    printf("\nReset:");


    tft.tft_setup();
    tft.setRotation(0);
    tft.fillScreen(ILI9341_BLACK);

    while (1) {
        setup();
        for (int i = 0; i < 200; i++) {
            loop();
        }
    }

    return 0;
}

void loop() {
    random_raspberry();
}

